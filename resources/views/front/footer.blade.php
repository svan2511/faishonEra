		$('#checkout_load_img').hide();
		if ( ajax_response.status=="failed" )
				{
					//console.log(ajax_response.error);
					let my_ul = '<ul>';
					$.each(ajax_response.error,function( key , value )
					{
						
						my_ul+= '<li class="item-link"><span class="cust_ul">'+value[0]+'</span></li>';
						
					});
					my_ul+='</ul>';
					$('#ul_div').html(my_ul);
					//console.log(my_ul);
					document.getElementById("main").scrollIntoView();
					return false;
				}
				if ( ajax_response.status=="O_P_S" )
				{
					$('#'+ajax_response.status).html(ajax_response.msg);
					window.location.href = '/order/details';
				}
				if ( ajax_response.status=="redirect_user" )
				{
					$('#O_P_S').html(ajax_response.msg);
					window.location.href = ajax_response.url_redirect;
				} 
				if ( ajax_response.status=="NPUD" )
				{
					$('#O_P_S').html(ajax_response.msg);
					return false;
				} 

				
			
    }
});

});


